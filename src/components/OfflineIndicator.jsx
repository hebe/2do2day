import React, { useState, useEffect } from 'react'

function OfflineIndicator() {
  const [isOnline, setIsOnline] = useState(navigator.onLine)

  useEffect(() => {
    const handleOnline = () => setIsOnline(true)
    const handleOffline = () => setIsOnline(false)

    window.addEventListener('online', handleOnline)
    window.addEventListener('offline', handleOffline)

    return () => {
      window.removeEventListener('online', handleOnline)
      window.removeEventListener('offline', handleOffline)
    }
  }, [])

  if (isOnline) return null

  return (
    <div className="fixed top-16 left-1/2 -translate-x-1/2 z-50 animate-slideDown">
      <div className="bg-orange-100 dark:bg-orange-900/90 border border-orange-300 dark:border-orange-700 rounded-lg px-4 py-2 shadow-lg flex items-center gap-2">
        <svg className="w-4 h-4 text-orange-600 dark:text-orange-400" fill="none" strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" viewBox="0 0 24 24" stroke="currentColor">
          <path d="M18.364 5.636a9 9 0 010 12.728m0 0l-2.829-2.829m2.829 2.829L21 21M15.536 8.464a5 5 0 010 7.072m0 0l-2.829-2.829m-4.243 2.829a4.978 4.978 0 01-1.414-2.83m-1.414 5.658a9 9 0 01-2.167-9.238m7.824 2.167a1 1 0 111.414 1.414m-1.414-1.414L3 3m8.293 8.293l1.414 1.414" />
        </svg>
        <span className="text-sm font-medium text-orange-900 dark:text-orange-100">
          You're offline
        </span>
      </div>
    </div>
  )
}

export default OfflineIndicator
